import { getElementNode } from '/@/models'
import { makeSvg } from '/@/utils/svgMaker'

describe('utils/svgMaker.ts', () => {
  describe('makeSvg', () => {
    it('make SVGElement from element node', () => {
      const ret = makeSvg(
        getElementNode({
          id: 'svg_1',
          tag: 'svg',
          attributes: {
            id: 'svg_1',
            xmlns: 'http://www.w3.org/2000/svg',
            viewBox: '1 2 3 4',
          },
          children: [
            getElementNode({
              id: 'g_1',
              tag: 'g',
              attributes: {
                id: 'g_1',
              },
              children: [
                getElementNode({
                  id: 'rect_1',
                  tag: 'rect',
                  attributes: {
                    id: 'rect_1',
                  },
                  children: [],
                }),
                getElementNode({
                  id: 'text_1',
                  tag: 'text',
                  attributes: {
                    id: 'text_1',
                  },
                  children: ['message'],
                }),
              ],
            }),
          ],
        })
      )

      expect(ret.getAttribute('viewBox')).toBe('1 2 3 4')
      expect(ret.getElementsByTagName('g').length).toBe(1)
      expect(ret.getElementsByTagName('rect').length).toBe(1)
      expect(ret.getElementsByTagName('text').length).toBe(1)
    })
  })
})
